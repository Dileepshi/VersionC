package Basics;

public class Switch {

	public static void main(String[] args) {
		
		int marks=10;
		
		switch(marks)
		
		{
			case 17:
		System.out.println("Bad");
		break;
			
		    case 5:
		System.out.println("passed");
		break;
		
		  case 10:
		System.out.println("super");
		break;
		
		default:
		System.out.println("all");
	}
		}

}
